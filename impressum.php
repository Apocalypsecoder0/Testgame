<b><u>Reasons for Project Creation</u></b><br /><br />

List of reasons why the Redesign is unsuitable for normal gameplay: 
<ul>
<li>Pages are heavier. Excessive traffic load.</li>
<li>Page rendering is very slow due to an abundance of images, styles, and overloaded JavaScript.</li>
<li>It is impossible to play properly from mobile devices.</li>
<li>Abundance of unfixable bugs. Constant emergence of new bugs.</li>
<li>Game balance changes (new planet activities in Overview (which are also buggy), Astrophysics, planet relocation, etc.).</li>
</ul>

As a result, the following is lost: 
<ul>
<li>The ability to adequately manage fleets. Now raising a fleet in 3-4 seconds or simultaneously slowing down 3-4 SCs is practically impossible.</li>
<li>With the corrected activity, a whole layer of the game is lost - the so-called "Ninja," where a player refreshes the Overview and suddenly catches an attacker through Gates.</li>
<li>Simple resource gathering from all planets or saving data on moons takes 2-3 times longer than before.</li>
<li>The chance to move a fleet in emergencies from a mobile device is reduced to zero.</li>
</ul>

<br /><br />
<b><u>Differences from the Original</u></b><br /><br />

What will not be in this project: 
<ul>
<li> The game will correspond to version 0.1.5</li>
<li>There will be no paid Dark Matter, but functions for Dark Matter found in expeditions will be available (Officers and Merchant).</li>
</ul>

Errors in the original version 0.1.5 will be fixed: 
<ul>
<li>Buggy planet when attempting to colonize the 10th planet. Instead, there will be an Abandoned Colony.</li>
<li>Target planet activity after returning your fleet to the starting planet.</li>
<li>Fleet cloning (raising fleet after an attack).</li>
<li>Negative resources (ordering construction during an attack).</li>
<li>Slowing down returning fleets recalled from SC.</li>
<li>Accelerating return from expeditions.</li>
<li>And others...</li>
</ul>

